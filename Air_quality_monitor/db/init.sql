CREATE TABLE IF NOT EXISTS air_quality (
    id SERIAL PRIMARY KEY,
    date DATE NOT NULL UNIQUE,
    pollen_level INTEGER NOT NULL CHECK (pollen_level BETWEEN 0 AND 5),
    dust_pm25 FLOAT NOT NULL,
    dust_pm10 FLOAT NOT NULL,
    temperature FLOAT,
    humidity FLOAT,
    wind_speed FLOAT
);

